'use client';

import { useState } from 'react';
import { Modal } from './ui';

export type LegalTab = 'privacy' | 'terms';

/**
 * Юридические документы — ШАБЛОН. Перед публичным запуском заполнить реальными
 * данными оператора (ФИО/организация, ИНН, контакты) и показать юристу.
 * Плейсхолдеры помечены [в квадратных скобках].
 */
export function LegalDialog({ open, tab, onClose }: { open: boolean; tab: LegalTab; onClose: () => void }) {
  const [active, setActive] = useState<LegalTab>(tab);
  if (!open) return null;

  return (
    <Modal
      title={active === 'privacy' ? 'Политика конфиденциальности' : 'Пользовательское соглашение'}
      subtitle="Черновик — заполняется перед публичным запуском"
      onClose={onClose}
      size="lg"
    >
      <div className="mb-4 flex gap-1 rounded-lg bg-bg p-1 text-sm">
        <button
          onClick={() => setActive('privacy')}
          className={`flex-1 rounded-md py-1.5 ${active === 'privacy' ? 'bg-surface-2 font-medium text-txt' : 'text-muted'}`}
        >
          Конфиденциальность
        </button>
        <button
          onClick={() => setActive('terms')}
          className={`flex-1 rounded-md py-1.5 ${active === 'terms' ? 'bg-surface-2 font-medium text-txt' : 'text-muted'}`}
        >
          Соглашение
        </button>
      </div>

      <div className="max-h-[60vh] overflow-auto pr-1 text-sm leading-relaxed text-muted">
        {active === 'privacy' ? <Privacy /> : <Terms />}
      </div>
    </Modal>
  );
}

function H({ children }: { children: React.ReactNode }) {
  return <h3 className="mt-4 mb-1 text-sm font-semibold text-txt first:mt-0">{children}</h3>;
}

